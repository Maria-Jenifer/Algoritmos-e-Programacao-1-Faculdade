# Escreva uma função que deverá receber duas cadeias de caracteres como parâmetros: nome1 e nome2. A função deverá retornar True se os valores forem iguais, ou False, caso contrário. 

def nomes_1_2(nome1, nome2):
    if nome1 == nome2:
        return True
    else:
        return False        
