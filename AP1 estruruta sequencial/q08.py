anoatual = int(input('Em qual ano você nasceu? '))

idade = 2023 - anoatual
imeses = idade * 12 
idias = idade * 365 
isemanas = idade * 52

print('A sua idade em anos é {}'.format(idade))
print('A sua idade em meses é {}'.format(imeses))
print('A sua idade em dias é {}'.format(idias))
print('A sua idade em semanas é {}'.format(isemanas))